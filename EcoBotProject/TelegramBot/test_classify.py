async def classify(session: aiohttp.ClientSession, query: str) -> dict:
    async with session.post(BASE_URL, json={"query": query}) as resp:
        return await resp.json()


async def run_tests():
    passed = 0
    failed = 0
    errors = 0

    connector = aiohttp.TCPConnector(limit=5)
    async with aiohttp.ClientSession(connector=connector) as session:
        tasks = [classify(session, q) for q, _ in TEST_CASES]
        results = await asyncio.gather(*tasks, return_exceptions=True)

    print(f"\n{'='*70}")
    print(f"  РЕГРЕССИОННЫЙ ТЕСТ SlotClassifier  ({len(TEST_CASES)} запросов)")
    print(f"{'='*70}\n")

    current_template = None
    for (query, expected), result in zip(TEST_CASES, results):
        if expected != current_template:
            current_template = expected
            print(f"\n── {expected} ──")

        if isinstance(result, Exception):
            errors += 1
            print(f"  ERR  {query!r}")
            print(f"       → {result}")
            continue

        got = result.get("template")
        err = result.get("error")

        if err:
            errors += 1
            print(f"  ERR  {query!r}")
            print(f"       → {err}")
        elif got == expected:
            passed += 1
            print(f"   ✓   {query!r}")
        else:
            failed += 1
            dbg = {
                "obj":   result.get("object_type"),
                "syn":   result.get("synonym"),
                "mod":   result.get("modality"),
                "props": result.get("properties"),
                "feat":  result.get("features"),
                "extra": result.get("extra"),
            }
            print(f"   ✗   {query!r}")
            print(f"       expected={expected}  got={got}")
            print(f"       {json.dumps(dbg, ensure_ascii=False)}")

    total = passed + failed + errors
    print(f"\n{'='*70}")
    print(f"  ИТОГО: {passed}/{total} пройдено | {failed} неверных | {errors} ошибок")
    acc = 100 * passed / total if total else 0
    print(f"  Точность: {acc:.1f}%")
    print(f"{'='*70}\n")


if __name__ == "__main__":
    asyncio.run(run_tests())
